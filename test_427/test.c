#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>
int main()
{
	char a = -128;//（char a = 128 结果相同）
	//10000000000000000000000010000000 - 原码
	//11111111111111111111111101111111 - 反码
	//11111111111111111111111110000000 - 补码（int）
	//10000000 - char a
	//11111111111111111111111110000000 - 整型提升（有符号数补符号位，无符号数补0）
	//无符号数 原码 补码 反码 相同
	//即打印
	printf("%u\n",a);

	//%d- 打印十进制的有符号数字
	//%u- 打印十进制的无符号数字
	return 0;
}